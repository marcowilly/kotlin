package com.example.myapplication

/**
 * Double 64
 * FLoat 32
 * Long 64
 * Int 32
 * Short 16
 * Byte 8
 */
fun main(){
    val b1: Byte = 100

    println("Short: ${b1.toShort()}")
    println("Int: ${b1.toInt()}")
    println("Long: ${b1.toLong()}")
    println("Float: ${b1.toFloat()}")
    println("Double: ${b1.toDouble()}")

    println("19".toInt())
    println("19".toFloat())
    println("19".toDouble())
}